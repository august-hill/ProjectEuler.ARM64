// Problem 042: Coded Triangle Numbers
// How many words in the file are triangle words?
// Answer: 162

.global _solve
.align 4

_solve:
    stp     x29, x30, [sp, #-16]!
    mov     x29, sp
    stp     x19, x20, [sp, #-16]!
    stp     x21, x22, [sp, #-16]!

    // Triangle numbers: t(n) = n*(n+1)/2
    // Max word value ~ 26*15 = 390, so generate triangles up to 30
    // Store 30 triangle numbers on stack (30*4 = 120, round to 128)
    sub     sp, sp, #128
    mov     x19, sp                  // triangle array

    mov     x1, #1
.Lgen_tri:
    cmp     x1, #30
    b.gt    .Lgen_tri_done
    add     x2, x1, #1
    mul     x2, x1, x2              // n*(n+1)
    lsr     x2, x2, #1              // /2
    sub     x3, x1, #1
    str     w2, [x19, x3, lsl #2]
    add     x1, x1, #1
    b       .Lgen_tri
.Lgen_tri_done:

    // Count triangle words
    mov     x20, #0                  // count
    adrp    x21, words@PAGE
    add     x21, x21, words@PAGEOFF  // x21 = pointer to words data

.Lword_loop:
    // Check if we've reached the end (null byte)
    ldrb    w1, [x21]
    cbz     w1, .Lword_done

    // Calculate word value
    mov     x22, #0                  // word_value
.Lchar_loop:
    ldrb    w1, [x21], #1
    cbz     w1, .Lcheck_triangle     // end of word (null terminator)
    sub     w1, w1, #'A'
    add     w1, w1, #1               // letter value (A=1, B=2, ...)
    add     x22, x22, x1
    b       .Lchar_loop

.Lcheck_triangle:
    // Check if word_value is a triangle number
    mov     x1, #0                   // index
.Ltri_check:
    cmp     x1, #30
    b.ge    .Lnot_triangle
    ldr     w2, [x19, x1, lsl #2]
    cmp     x22, x2
    b.eq    .Lis_triangle
    cmp     x2, x22
    b.gt    .Lnot_triangle
    add     x1, x1, #1
    b       .Ltri_check

.Lis_triangle:
    add     x20, x20, #1
.Lnot_triangle:
    b       .Lword_loop

.Lword_done:
    mov     x0, x20

    add     sp, sp, #128
    ldp     x21, x22, [sp], #16
    ldp     x19, x20, [sp], #16
    ldp     x29, x30, [sp], #16
    ret

